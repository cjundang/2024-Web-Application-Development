

// Primitive data types
const name = "React"; // String
const version = 18; // Number
const isActive = true; // Boolean
console.log(typeof name, typeof version, typeof isActive); // Output: string number boolean

// Reference data types
const user = { id: 1, name: "John" }; // Object
const skills = ["JavaScript", "React"]; // Array
console.log(typeof user, typeof skills); // Output: object object

// Accessing reference data
console.log(user.name); // Output: John
console.log(skills[0]); // Output: JavaScript

