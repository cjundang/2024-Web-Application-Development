// Arithmetic Operators
const a = 10;
const b = 5;
console.log(a + b, a - b, a * b, a / b); // Output: 15 5 50 2

// Comparison Operators
console.log(a === b); // Output: false
console.log(a > b); // Output: true

// Logical Operators
const isLoggedIn = true;
const hasAccess = true;
console.log(isLoggedIn && hasAccess); // Output: true
console.log(isLoggedIn || hasAccess); // Output: true
console.log(!isLoggedIn); // Output: false

// Ternary Operator
const isAdmin = true;
console.log(isAdmin ? "Admin Panel" : "User Dashboard"); // Output: Admin Panel
